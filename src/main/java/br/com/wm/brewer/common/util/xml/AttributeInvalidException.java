package br.com.wm.brewer.common.util.xml;

public class AttributeInvalidException extends RuntimeException {
	public AttributeInvalidException() {
		super();
	}

	public AttributeInvalidException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AttributeInvalidException(String arg0) {
		super(arg0);
	}

	public AttributeInvalidException(Throwable arg0) {
		super(arg0);
	}
	
}
