package br.com.wm.brewer.common.util.xml;

public class ElementXmlException extends RuntimeException {
	public ElementXmlException() {
		super();
	}

	public ElementXmlException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ElementXmlException(String arg0) {
		super(arg0);
	}

	public ElementXmlException(Throwable arg0) {
		super(arg0);
	}
	
}
